#####
# Calculation of the area and circumference of a circle
##
import math

# determine radius and PI
radius=5    # ... program statements here ...
PI=math.pi

# calculate area 
Area=PI*radius**2   # ... program statements here ...
print(Area)

# calculate circumference 
Circumference=2*PI*radius   # ... program statements here ...
print(Circumference)
# display results 
# ... program statements here ...
print(f"Promień wynosi:{radius}, Pole wynosi:{Area}, Obwód wynosi:{Circumference}")
